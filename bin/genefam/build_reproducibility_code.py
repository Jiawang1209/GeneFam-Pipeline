#!/usr/bin/env python3
"""Write a Markdown file containing the commands needed to reproduce an analysis run."""

from __future__ import annotations

import argparse
from pathlib import Path


def build_markdown(
    config: Path,
    clean_species_manifest: Path,
    reference_manifest: Path,
    family_candidates: Path,
    config_label: str | None = None,
    groups_label: str = "configs/species_groups.yaml",
    outdir: str = "results/standard",
    preprocess_outdir: str = "results/01_preprocess",
    clean_species_manifest_label: str | None = None,
    reference_manifest_label: str | None = None,
    family_candidates_label: str | None = None,
) -> str:
    config_for_command = config_label or str(config)
    clean_species_manifest_for_report = clean_species_manifest_label or str(clean_species_manifest)
    reference_manifest_for_report = reference_manifest_label or str(reference_manifest)
    family_candidates_for_report = family_candidates_label or str(family_candidates)
    return "\n".join(
        [
            "# Analysis Reproducibility Code",
            "",
            "This file records the command-level reproduction path for the current GeneFam-Pipeline analysis.",
            "",
            "## Inputs",
            "",
            f"- Config: `{config_for_command}`",
            f"- Groups: `{groups_label}`",
            f"- Standard output directory: `{outdir}`",
            f"- Preprocess output directory: `{preprocess_outdir}`",
            f"- Clean species manifest: `{clean_species_manifest_for_report}`",
            f"- Reference manifest: `{reference_manifest_for_report}`",
            f"- Family candidates: `{family_candidates_for_report}`",
            "",
            "## 01_preprocess",
            "",
            "```bash",
            "python bin/genefam/discover_species.py \\",
            f"  --config {config_for_command} \\",
            f"  --groups {groups_label} \\",
            "  --base-dir . \\",
            f"  --out {preprocess_outdir}/species_manifest.raw.tsv",
            "",
            "python bin/genefam/preprocess_species.py \\",
            f"  --species-manifest {preprocess_outdir}/species_manifest.raw.tsv \\",
            f"  --outdir {preprocess_outdir}",
            "```",
            "",
            "## Reference Generation",
            "",
            "```bash",
            "python bin/genefam/build_reference_from_config.py \\",
            f"  --config {config_for_command} \\",
            f"  --species-manifest {preprocess_outdir}/species_manifest.clean.tsv \\",
            "  --base-dir . \\",
            f"  --outdir {preprocess_outdir}/reference",
            "```",
            "",
            reference_manifest.read_text(encoding="utf-8").strip(),
            "",
            "## Standard Nextflow Run",
            "",
            "```bash",
            'PATH="/Users/liuyue/miniforge3/envs/GeneFamilyFlow/bin:$PATH" \\',
            "nextflow run workflows/main.nf \\",
            "  -c workflows/nextflow.config \\",
            "  -profile activated \\",
            f"  --config {config_for_command} \\",
            f"  --groups {groups_label} \\",
            "  --run_identification true \\",
            "  --use_hmmer true \\",
            "  --use_diamond true \\",
            "  --final_rule intersection \\",
            f"  --outdir {outdir} \\",
            f"  --preprocess_outdir {preprocess_outdir}",
            "```",
            "",
            "## Key Outputs",
            "",
            f"- `{preprocess_outdir}/species_manifest.clean.tsv`",
            f"- `{preprocess_outdir}/reference/PF00657.reference.pep.fa`",
            f"- `{family_candidates_for_report}`",
            f"- `{outdir}/report/final_report.md`",
            "",
            "## Reference-Level Module Commands And Handoffs",
            "",
            "The formal Nextflow run above executes these Reference-aligned modules. The snippets below document the script-level handoffs and output files to inspect or rerun when debugging a specific stage.",
            "",
            "### MEME motif analysis",
            "",
            "- Nextflow processes: `RUN_MEME_MOTIFS`, `PARSE_MEME_MOTIFS`",
            f"- Outputs: `{outdir}/meme/`, `{outdir}/tables/motif_summary.tsv`, `{outdir}/analysis_modules/05_motif_analysis/`",
            "",
            "### JCVI inter-species collinearity",
            "",
            "```bash",
            "python bin/genefam/prepare_jcvi_collinearity.py \\",
            f"  --family-candidates {family_candidates_for_report} \\",
            f"  --species-manifest {clean_species_manifest_for_report} \\",
            "  --outdir <jcvi_collinearity_outdir>",
            "",
            "python bin/genefam/run_jcvi_collinearity.py \\",
            "  --prepared-dir <jcvi_collinearity_outdir> \\",
            "  --outdir <jcvi_collinearity_outdir>",
            "```",
            f"- Outputs: `{outdir}/jcvi_collinearity/`, `{outdir}/analysis_modules/10_synteny_jcvi/`",
            "",
            "### MCScanX self intra-species duplication and circlize",
            "",
            "```bash",
            "python bin/genefam/build_mcscanx_self_inputs.py \\",
            f"  --family-candidates {family_candidates_for_report} \\",
            f"  --species-manifest {clean_species_manifest_for_report} \\",
            "  --search-tool diamond \\",
            "  --outdir <mcscanx_self_outdir>",
            "",
            "python bin/genefam/run_mcscanx_self.py \\",
            "  --prepared-dir <mcscanx_self_outdir> \\",
            "  --execute",
            "```",
            f"- Outputs: `{outdir}/mcscanx_self_circos/`, `{outdir}/plots/mcscanx_circlize.pdf`, `{outdir}/analysis_modules/11_mcscanx/`",
            "",
            "### JCVI/MCScanX KaKs and WGD layers",
            "",
            "```bash",
            "python bin/genefam/prepare_reference_kaks_inputs.py \\",
            f"  --species-manifest {clean_species_manifest_for_report} \\",
            f"  --jcvi-dir {outdir}/jcvi_collinearity \\",
            f"  --pair-source mcscanx={outdir}/mcscanx_self_circos/mcscanx_gene_pairs.tsv \\",
            "  --outdir <kaks_inputs_outdir>",
            "",
            "python bin/genefam/run_reference_kaks_calculator.py \\",
            "  --manifest <kaks_inputs_outdir>/kaks_input_manifest.tsv \\",
            "  --outdir <kaks_inputs_outdir> \\",
            "  --executable KaKs_Calculator",
            "```",
            f"- Outputs: `{outdir}/kaks_inputs/kaks_pairs.tsv`, `{outdir}/kaks_inputs/kaks_failure_summary.tsv`, `{outdir}/tables/wgd_event_evidence.tsv`, `{outdir}/plots/ks_distribution.pdf`, `{outdir}/analysis_modules/14_duplication_retention_kaks/`",
            "",
            "### Promoter and PlantCARE handoff",
            "",
            "```bash",
            "python bin/genefam/extract_promoters.py \\",
            f"  --family-candidates {family_candidates_for_report} \\",
            f"  --species-manifest {clean_species_manifest_for_report} \\",
            "  --upstream-bp 2000 \\",
            "  --downstream-bp 0 \\",
            "  --out-bed <promoter_outdir>/promoters.bed \\",
            "  --out-fasta <promoter_outdir>/promoters.fa",
            "",
            "python bin/genefam/split_promoter_fasta_for_plantcare.py \\",
            "  --promoter-fasta <promoter_outdir>/promoters.fa \\",
            "  --outdir <promoter_outdir>/plantcare_submission \\",
            "  --records-per-file 100 \\",
            "  --prefix GDSL_promoters",
            "```",
            f"- Upload `{outdir}/plantcare_submission/*.fa` to PlantCARE, then set `promoter.cis_elements` in the YAML to the returned gene-level table and rerun the workflow.",
            f"- Handoff files: `{outdir}/plantcare_submission/plantcare_submission_manifest.tsv`, `{outdir}/plantcare_submission/plantcare_submission_status.tsv`",
            "",
            "### AraNet PPI transfer and ggNetView",
            "",
            "```bash",
            "python bin/genefam/build_aranet_ppi_from_reciprocal_blast.py \\",
            f"  --family-candidates {family_candidates_for_report} \\",
            f"  --species-manifest {clean_species_manifest_for_report} \\",
            "  --aranet-edges <AraNet.txt> \\",
            "  --reference-species Arabidopsis_thaliana \\",
            "  --outdir <ppi_outdir>",
            "```",
            "- Nextflow plotting process: `PLOT_PPI_GGNETVIEW`",
            f"- Outputs: `{outdir}/tables/ppi_edges.tsv`, `{outdir}/plots/ppi_ggnetview.pdf`, `{outdir}/analysis_modules/12_ppi/`",
            "",
            "### Final report and module package",
            "",
            "```bash",
            "python bin/genefam/organize_module_results.py \\",
            f"  --source {outdir} \\",
            f"  --outdir {outdir}/analysis_modules \\",
            f"  --final-report {outdir}/report/final_report.md",
            "```",
            f"- Final report: `{outdir}/report/final_report.md`",
            f"- Module package: `{outdir}/analysis_modules/`",
            f"- Reproducibility code: `{outdir}/report/reproducibility_code.md`",
            "",
        ]
    )


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--config", required=True, type=Path)
    parser.add_argument("--clean-species-manifest", required=True, type=Path)
    parser.add_argument("--reference-manifest", required=True, type=Path)
    parser.add_argument("--family-candidates", required=True, type=Path)
    parser.add_argument("--config-label")
    parser.add_argument("--groups-label", default="configs/species_groups.yaml")
    parser.add_argument("--outdir", default="results/standard")
    parser.add_argument("--preprocess-outdir", default="results/01_preprocess")
    parser.add_argument("--clean-species-manifest-label")
    parser.add_argument("--reference-manifest-label")
    parser.add_argument("--family-candidates-label")
    parser.add_argument("--out", required=True, type=Path)
    args = parser.parse_args()
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(
        build_markdown(
            args.config,
            args.clean_species_manifest,
            args.reference_manifest,
            args.family_candidates,
            config_label=args.config_label,
            groups_label=args.groups_label,
            outdir=args.outdir,
            preprocess_outdir=args.preprocess_outdir,
            clean_species_manifest_label=args.clean_species_manifest_label,
            reference_manifest_label=args.reference_manifest_label,
            family_candidates_label=args.family_candidates_label,
        ),
        encoding="utf-8",
    )
    print(f"Wrote reproducibility code Markdown: {args.out}")


if __name__ == "__main__":
    main()
